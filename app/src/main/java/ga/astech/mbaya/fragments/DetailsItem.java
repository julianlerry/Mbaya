public class DetailsItem {

    private String textView;
    private String costView;
    private String descriptionView;
    private String cityView;
    private String dateView;
    private int imageView;

    public String getCostView() {
        return costView;
    }

    public void setCostView(String costView) {
        this.costView = costView;
    }

    public String getDescriptionView() {
        return descriptionView;
    }

    public void setDescriptionView(String descriptionView) {
        this.descriptionView = descriptionView;
    }

    public String getCityView() {
        return cityView;
    }

    public void setCityView(String cityView) {
        this.cityView = cityView;
    }

    public String getDateView() {
        return dateView;
    }

    public void setDateView(String dateView) {
        this.dateView = dateView;
    }

    public String getTextView() {
        return textView;
    }

    public void setTextView(String textView) {
        this.textView = textView;
    }

    public int getImageView() {
        return imageView;
    }

    public void setImageView(int imageView) {
        this.imageView = imageView;
    }
}
